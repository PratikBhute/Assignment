package CalculationDemo;

public class Calc {

    public static int Add(int a, int b)
        {
            return a+b;
        }
    public int sub(int a, int b)
    {
        return a-b;
    }
    public int multy(int a, int b)
    {
        return a*b;
    }
    public int div(int a, int b)
    {
        return a/b;
    }
    public int module(int a, int b)
    {
        return a%b;
    }
    public int square(int a)
        {
            return a*a;
        }
    }

